    private void SubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitButtonActionPerformed
        // Get data from each client related text field
        String name = NamejTextField1.getText();
        String email = emailAddress.getText();
        int retirementAge = Integer.parseInt(RetirementAgejTextField3.getText());
        int canadaYears = Integer.parseInt(CanadaYearsjTextField4.getText());
        int age = Integer.parseInt(AgejTextField1.getText());
        int futureIncome = Integer.parseInt(ExpectedRetirementIncome.getText());
        
        //Change client data based on textfield values
        clients[clientnumber].changeName(name);
        clients[clientnumber].changeAge(age);
        clients[clientnumber].changeAgeRetirement(retirementAge);
        clients[clientnumber].changeEmail(email);
        clients[clientnumber].changeCanadaYears(canadaYears);
        clients[clientnumber].changeFutureRetirementIncome(futureIncome);
        
        // get data from each pension related text field
        int income = Integer.parseInt(IncomeText.getText());
        int yearsSalary = Integer.parseInt(YearsSalary.getText());
        int definedCB = Integer.parseInt(DefinedCB.getText());
        int currentSavings = Integer.parseInt(CurrentSavings.getText());
        
        int contributionPrice;

        if (contribution == true) {
            contributionPrice = 300;
        }
        else {
            contributionPrice = 0;
        }
        
        // Change pension data based on textfield values
        pension[clientnumber].changeIncome(income);
        pension[clientnumber].changeYearsofSalary(yearsSalary);
        pension[clientnumber].changeDefinedCompanyBenefit(definedCB);
        pension[clientnumber].changeDefinedContribution(contribution);
        pension[clientnumber].changeCurrentSavings(currentSavings);

        this.setVisible(false);
        first.setVisible(true);
    }//GEN-LAST:event_SubmitButtonActionPerformed

