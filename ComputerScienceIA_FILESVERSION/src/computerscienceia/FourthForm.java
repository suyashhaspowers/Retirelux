    private void UpdateListButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateListButtonActionPerformed
    DefaultListModel DL = new DefaultListModel();
        
        String tempStr;
        
//      Creating List/Collection of name

        List<String> name = new ArrayList<String>();
        
        // Assigning values to the collection from array of Clients
        for (int t=0; t <= num-1; t++) {
            String clientName = (clients[t].getName()+" - Client Number: "+t);
            name.add(clientName);
        }
        

        //IF there is more than zero clients
        if (emptyData == true){
            
            //If only one client has been entered into system
            if (num == 0) {
                DL.addElement(clients[0].getName()+" - Client Number: 0");
                jList1.setModel(DL);
            }
            //If more than one client has been entered
            else {
                for (int t = 0; t < num-1; t++) {
                    for (int i= 0; i < num - t -1; i++) {
                        if((name.get(i)).compareTo((name.get(i+1)))<0) {
                            tempStr = name.get(i);
                            name.set(i,name.get(i+1));
                            name.set(i+1, tempStr);
                        }
                    }
                }

                for (int b = 1; b <= num; b++) {
                    //place name on list
                    DL.addElement(name.get((num-b)));
                    jList1.setModel(DL);
                }
            }
 
               
        }
        // If there are no clients
        else {
            String z = "Create a Client in the Main Menu";
            DL.addElement(z);
            jList1.setModel(DL);
        }
    }//GEN-LAST:event_UpdateListButtonActionPerformed

